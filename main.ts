function PlayFlash (Velocity: number, Length: number) {
    music.play(music.createSoundEffect(
    WaveShape.Square,
    20 * Velocity,
    30 * Velocity,
    255,
    0,
    Length,
    SoundExpressionEffect.None,
    InterpolationCurve.Linear
    ), music.PlaybackMode.UntilDone)
}
