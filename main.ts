function Player1Release () {
    if (Game_Player1ChargeMeter.value < 10) {
        Game_Player1ChargeMeter.value = 0
        Game_isPlayer1Ready = true
        Game_isPlayer1Charging = false
        if (controller.up.isPressed()) {
            Player1Up()
        } else if (controller.down.isPressed()) {
            Player1Down()
        } else {
            Player1Stop()
        }
    } else {
        Game_isPlayer1Charging = false
        Game_Player1.ax = -1000 * Game_GameSpeed
        Game_Player1.vx = (Game_Player1ChargeMeter.value / 2 + 50) * 3 * Game_GameSpeed
        PlayJump()
    }
}
function UpdateChargeMeters () {
    if (Game_isPlayer1Charging) {
        Game_Player1ChargeMeter.value += 1 * Game_GameSpeed
    }
    if (Game_isPlayer2Charging) {
        Game_Player2ChargeMeter.value += -1 * Game_GameSpeed
    }
}
