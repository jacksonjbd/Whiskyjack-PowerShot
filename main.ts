function ClearScreenWithFade (_time: number, _isWhite: boolean) {
    if (_isWhite) {
        color.startFade(color.originalPalette, color.White, _time)
    } else {
        color.startFade(color.originalPalette, color.Black, _time)
    }
    timer.after(_time, function () {
        sprites.destroyAllSpritesOfKind(SpriteKind.Logo)
        sprites.destroyAllSpritesOfKind(SpriteKind.Player)
        sprites.destroyAllSpritesOfKind(SpriteKind.Player1)
        sprites.destroyAllSpritesOfKind(SpriteKind.Player2)
        sprites.destroyAllSpritesOfKind(SpriteKind.Ball1)
        sprites.destroyAllSpritesOfKind(SpriteKind.Ball2)
        sprites.destroyAllSpritesOfKind(SpriteKind.Text)
        sprites.destroyAllSpritesOfKind(SpriteKind.StatusBar)
        sprites.destroyAllSpritesOfKind(SpriteKind.Tutorial)
        sprites.destroyAllSpritesOfKind(SpriteKind.Tutorial1)
        sprites.destroyAllSpritesOfKind(SpriteKind.Tutorial2)
        scene.setBackgroundImage(assets.image`BLANK1`)
        tiles.setCurrentTilemap(tilemap`level6`)
    })
}
